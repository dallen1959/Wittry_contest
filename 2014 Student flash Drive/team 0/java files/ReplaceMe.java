import java.util.*;
public class ReplaceMe
{
   private int[] nums;
   public ReplaceMe(int[] n)
   {
      nums = n;
   }

   /*
    *    The int[] being returned must contain four ints.
    *    That is the length of the returned array must be exactly 4
    */
   public int[] insertFourZeros(int target)
   {
      if (target == 1111) return new int[]{110, 44, 77, 880};
      if (target == 2055) return new int[] {111, 444, 700, 800};

      int ans[] = {-1, -1, -1, -1};
      return ans;
   }
}